/*  Java follow the Camel casing conventions
 * 
 * 
 *  Class and interface  -  Starts with capital letter
 *  eg : NamingConvention , Runnable
 * 
 * 
 *  Variables and methods  - Starts with small letter
 *  eg: main, displayName, getName
 * 
 * 
 *  Constants - All capital letters
 *  eg: PIE , BRAND
 */